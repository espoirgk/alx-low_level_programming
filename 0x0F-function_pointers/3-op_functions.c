#include "3-calc.h"
/**
 * op_add - addition of two integers
 * @a: integer1
 * @b: integer2
 * Return: sum a, b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract
 * @a: integer1
 * @b: integer2
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: integer1
 * @b: integer2
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: integer1
 * @b: integer2
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: integer1
 * @b: integer2
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
